// Check Positive, Negative or Zero

const a= -5;
if(a>0){
    console.log(`This Number is Positive: ${a}`)
}else if(a < 0){
    console.log(`This Number is Negative: ${a}`)
}else{
    console.log(`This Number is Zero: ${a}`)
}

// ------Sample Input/Output:

// Input: -5 → Output: Negative
// Input: 0 → Output: Zero
// Input: 12 → Output: Positive