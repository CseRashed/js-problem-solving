// Student Performance Category (if...else if)

const marks= 80;
if(marks>=90){
    console.log('Excellent')
}else if(marks >=75){
    console.log('Very Good')
}else if(marks>=50){
    console.log('Average')
}else{
    console.log('Needs Improvement')
}

